package com.danyfahrurrozi.forex;

public class RatesModel
{
    private double CHF, SAR, CUP, EUR, IDR, ILS, RUB, USD, LYD, IQD;

    public RatesModel() {}

    public double getCHF() { return CHF; }

    public void setCHF(double CHF) { this.CHF = CHF; }

    public double getSAR() { return SAR; }

    public void setSAR(double SAR) { this.SAR = SAR; }

    public double getCUP() { return CUP; }

    public void setCUP(double CUP) { this.CUP = CUP; }

    public double getEUR() { return EUR; }

    public void setEUR(double EUR) { this.EUR = EUR; }

    public double getIDR() { return IDR; }

    public void setIDR(double IDR) { this.IDR = IDR; }

    public double getILS() { return ILS; }

    public void setILS(double ILS) { this.ILS = ILS; }

    public double getRUB() { return RUB; }

    public void setRUB(double RUB) { this.RUB = RUB; }

    public double getUSD() { return USD; }

    public void setUSD(double USD) { this.USD = USD; }

    public double getLYD() { return LYD; }

    public void setLYD(double LYD) { this.LYD = LYD; }

    public double getIQD() { return IQD; }

    public void setIQD(double IQD) { this.IQD = IQD; }
}
